    public void CambiarEstado()
    {
    
        try 
        {
            if(txtIDpago.getText().length()==0)
            { 
                JOptionPane.showMessageDialog(null,"Debes Seleccionar un registro de tu tabla");
                
            }else
            {
 
            String SQL = "update pagolibro set IDcompra = ?  where IDpago = ?";
            int filasSeleccionada = tblRegistros.getSelectedRow();
            String dao = (String) tblRegistros.getValueAt(filasSeleccionada, 0); // Fila seleccionada actulizamos la tabla
            PreparedStatement pst=con.prepareStatement(SQL); // Preparamos la consulta
             // Preparamos para insertar los datos correspondiente 
             pst.setInt(1,cbxEstado.getSelectedIndex());
             // Guardamos los datos 
             pst.setString(2,dao);
             pst.execute();
            JOptionPane.showMessageDialog(null, "Registros Actualizado"); // Mostramos un mensjae que se guardar
            MostrarDatos(); // Recargamos la tabla
            txtIDpago.setText(""); // Limpiamos la caja de texto
            }
            
        } catch (Exception e) 
        {
            JOptionPane.showMessageDialog(null, "Error de Actualizar" +e );
        }
        
    }
    
