    // Metodo para buscar 
    public void buscador(String valor)
    {
        // Creamos la cabecera de la tabla.
        String[] titulos = {"N°","Nombre","Pago","Reposición","Estado"};
        String[] registros = new String[5];
        
        DefaultTableModel modelo = new DefaultTableModel(null,titulos);
        //Realizamos la consulta para mostrar los datos    
        String SQL = "select pago.IDpago , user.Nombre , pago.PagoLibro , pago.ReposiciónLibro , estado.Nombre as 'Estado'from pagolibro as pago inner join usuarios as user on user.IDusuario = pago.IDusuario left join est_compra as estado on pago.IDcompra = estado.IDcompra  where pago.ReposiciónLibro like '%"+valor+"%' or estado.Nombre like '%"+valor+"%'";
        
        try 
        {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            
            while(rs.next())
             {
               registros[0] = rs.getString("IDpago");
               registros[1] = rs.getString("Nombre");
               registros[2] = rs.getString("PagoLibro");
               registros[3] = rs.getString("ReposiciónLibro");
               registros[4] = rs.getString("Estado");
               modelo.addRow(registros);  
             }// Fin de while 
             tblRegistros.setModel(modelo);
            
        } catch (Exception e) 
        {
           JOptionPane.showMessageDialog(null, "Error al mostrar datos Busqueda" + e.getMessage());   
        }
        
        
    }// Fin del Metodo de buscar
    
