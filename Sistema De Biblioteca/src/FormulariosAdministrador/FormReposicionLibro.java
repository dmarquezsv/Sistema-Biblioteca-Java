    public  void MostrarDatos()
    {
        // Creamos la cabecera de la tabla.
        String[] titulos = {"N°","Nombre","Pago","Reposición","Cantidad","Estado"};
        String[] registros = new String[6];
        
        DefaultTableModel modelo = new DefaultTableModel(null,titulos);
        //Realizamos la consulta para mostrar los datos    
        String SQL = "select pago.IDpago , user.Nombre , pago.PagoLibro , pago.ReposiciónLibro,pago.Cantidad , estado.Nombre as 'Estado'from pagolibro as pago inner join usuarios as user on user.IDusuario = pago.IDusuario left join est_compra as estado on pago.IDcompra = estado.IDcompra;";
        try 
        {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(SQL);
             // Reccoremos la tabla para traer dicha información de cada empleado
             while(rs.next())
             {
               registros[0] = rs.getString("IDpago");
               registros[1] = rs.getString("Nombre");
               registros[2] = rs.getString("PagoLibro");
               registros[3] = rs.getString("ReposiciónLibro");
               registros[4] = rs.getString("Cantidad");
               registros[5] = rs.getString("Estado");
               modelo.addRow(registros);  
             }// Fin de while 
             tblRegistros.setModel(modelo);
             
            
        } catch (Exception e) 
        {
           JOptionPane.showMessageDialog(null, "Error al mostrar datos" + e.getMessage()); // Verificar el usuario 
        }

    }
    
