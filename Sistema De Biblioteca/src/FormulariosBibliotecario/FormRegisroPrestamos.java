 public void InsertarRegistro()
 {
     try 
     {
          //Variable que almacenara para insertar datos a la BD
          String SQL = "insert into prestamo (IDusuario,IDlibro,FechaEntrega,FechaDevolucion,EstadoLibro,Cantidad,Hora,renovaciones) values (?,?,?,?,?,?,?,?)"; 
          PreparedStatement pst=con.prepareStatement(SQL); // Prepara mos la consulta
          int año = jdtEntrega.getCalendar().get(Calendar.YEAR);
          int mes = jdtEntrega.getCalendar().get(Calendar.MARCH);
          int dia = jdtEntrega.getCalendar().get(Calendar.DAY_OF_MONTH);
          String Entrega =  dia+"-"+mes+"-"+año;
          
          int año2 = jdtDevolucion.getCalendar().get(Calendar.YEAR);
          int mes2 = jdtDevolucion.getCalendar().get(Calendar.MARCH);
          int dia2= jdtDevolucion.getCalendar().get(Calendar.DAY_OF_MONTH);
          String Devolucion = dia2+"-"+mes2+"-"+año2;
          
           //Hora de registro
            Calendar calendario = Calendar.getInstance();
            int hora, minutos, segundos;
            hora =calendario.get(Calendar.HOUR_OF_DAY);
            minutos = calendario.get(Calendar.MINUTE);
            segundos = calendario.get(Calendar.SECOND);
            String  HoraRegistro = hora + ":" + minutos + ":" + segundos; // Extraemos la hora de registro
          
          String NumeroRenovacion="0";
          
          
          
         
             // Si todo esta correcto mandaremos la información a cada columna
            pst.setString(1, txtIDUsuario.getText());
            pst.setString(2, txtIDLibro.getText());
            pst.setString(3,Entrega);
            pst.setString(4,Devolucion);
            pst.setString(5,(String)jcbEstado.getSelectedItem());
            pst.setString(6,txtCantidad.getText());
            pst.setString(7,HoraRegistro);
            pst.setString(8,NumeroRenovacion);
            
            int n = pst.executeUpdate();
            if(n > 0)
            {
              JOptionPane.showMessageDialog(null, "Registro con exito");
              txtIDUsuario.setText("");
              txtIDLibro.setText("");
              txtCantidad.setText("");
            
          }
     } 
     catch (Exception e) 
     {
        JOptionPane.showMessageDialog(null, "Error Verifique:  " + e);  
     }
 }// Fin del METODO PARA INSERTAR PRESTAMOS DE LOS LIBROS
    
    
 
