    private void btnRegistrarReservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarReservaActionPerformed

        String ncantidades = txtVerifcarCantida.getText();
        if(ncantidades.equals("0"))
        {
          JOptionPane.showMessageDialog(null,"No hay libros por el momento.");  
        }else{


        if(txtIDUsuario.getText().length()==0)
          {
              JOptionPane.showMessageDialog(null,"Seleccione en la tabla de usuarios para extraer el N° Registro"); 
          }
          else if(txtIDLibro.getText().length()==0)
          {
              JOptionPane.showMessageDialog(null,"Seleccione en la tabla de libro para extraer el N° Registro");   
          }
          else if(jdtEntrega.getDate()==null)
          {
              JOptionPane.showMessageDialog(null,"Ingrese la fecha de entrega");   
          }
          else if(jdtDevolucion.getDate()==null)
          {
              JOptionPane.showMessageDialog(null,"Ingrese la fecha de devolución");   
          }
          else if(txtCantidad.getText().length()==0)
          {
              JOptionPane.showMessageDialog(null,"No has ingresado la cantidad de libro solicitado");   
          }
          else if(jdtEntrega.getDate()==null)
          {
               JOptionPane.showMessageDialog(null,"No has ingresado la fecha de entrada");
          }
          else if(jdtDevolucion.getDate()==null)
          {
             JOptionPane.showMessageDialog(null,"No has ingresado la fecha devolución"); 
          }
          else
          {
             ActualizarInventario();
             MostrarDatos();  
          }
        }
       
    }//GEN-LAST:event_btnRegistrarReservaActionPerformed

