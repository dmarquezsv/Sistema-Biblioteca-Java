   public void ActualizarMulta()
   {
       if(txtIDMulta.getText().length()==0)
       {
           JOptionPane.showMessageDialog(null,"Debes Seleccionar al asurio que muestra en la tabla de multa");
       }
       else if (txtRegistrarDinero.getText().length()==0) 
       {
           JOptionPane.showMessageDialog(null,"Ingrese la cantidad de dinero");
       }else
       {
           try {
                // Por medio de la conuslta actulizamos el emppleado la cual en where le indicamos que se actualizar por medio del id
           String SQL = "update registromulta set Multa=?,Tipo=?,Estado=?  where IDregistro=?";
           
           int filasSeleccionada = tblPrestamos.getSelectedRow();
                    String dao = (String) tblPrestamos.getValueAt(filasSeleccionada, 0); // Fila seleccionada actulizamos la tabla
                    PreparedStatement pst=con.prepareStatement(SQL); // Preparamos la consulta
                    // Preparamos para insertar los datos correspondiente
                    pst.setString(1,txtRegistrarDinero.getText());
                    pst.setString(2,(String)cbxTipo.getSelectedItem());
                    pst.setString(3,(String)cbxEstado.getSelectedItem());
          // Guardamos los datos 
          pst.setString(4,dao);
          pst.execute();
          JOptionPane.showMessageDialog(null, "Registros Actualizado"); // Mostramos un mensaje
          txtRegistrarDinero.setText("");
          txtNumeUser.setText("");
                    
           } catch (Exception e) 
           {
               JOptionPane.showMessageDialog(null, "Error de Actualizar" + e );
           }
       
       }
   }
   
